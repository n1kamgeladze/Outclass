a = int(input("enter the first number: "))

choice = input("enter your choice: ")

b = int(input("enter the second number: "))


if choice == '+':
    print(a + b)
elif choice == "-" :
    print(a - b)
elif choice == "*" :
    print(a * b)
elif choice == '/':
    print(a / b)
else: 
    print("error")

